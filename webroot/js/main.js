    function loadDoc(url) {
        var xhttp = new XMLHttpRequest();
        url = url + "?fname=" + document.f1.fname.value + "&email=" + document.f1.email.value + "&message=" + document.f1.message.value;


        xhttp.onreadystatechange = function() {
            if (this.readyState == 4 && this.status == 200) {
                if (this.responseText == 'failed') {
                    document.getElementById("alert-msg").innerHTML = "please try again";
                    document.getElementById("alert-msg").style.color = "red";
                } else
                    document.getElementById("alert-msg").innerHTML = "message sent sucessfully";

            } else {
                document.getElementById("alert-msg").innerHTML = "please try again";
                document.getElementById("alert-msg").style.color = "red";

            }
        };
        xhttp.open("GET", url, true);
        xhttp.send();
    }


